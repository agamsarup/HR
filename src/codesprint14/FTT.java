	static void solve()
	{
		int n = ni();
		int Q = ni();
		int[] b = na(n);
		Arrays.sort(b);
		int m = b[n-1];
		
		Deque<Integer> dq = new ArrayDeque<Integer>();
		int[] d = new int[m];
		int I = 9999999;
		Arrays.fill(d, I);
		dq.addLast(0);
		d[0] = 0;
		while(!dq.isEmpty()){
			int cur = dq.pollFirst();
			for(int i = 0;i < n-1;i++){
				if(cur+b[i] >= m){
					if(d[cur+b[i]-m] > d[cur] + 1){
						d[cur+b[i]-m] = d[cur] + 1;
						dq.addLast(cur+b[i]-m);
					}
				}else{
					if(d[cur+b[i]] > d[cur]){
						d[cur+b[i]] = d[cur];
						dq.addFirst(cur+b[i]);
					}
				}
			}
		}
		for(int z = 0;z < Q;z++){
			int u = ni();
			out.println(u/m >= d[u % m] ? "yes" : "no");
		}
	}
	
